// Write a function that takes a string as a parameter.
// The function must return the number of repeated characters, even uppercase or lowercase.

function myFunction(a) {
  let aSplit = a.split(" ");
  for (let i = 0; i < aSplit.length; i++) {}
}

console.log(myFunction("abcde"));

console.log(myFunction("aabbccde"));

console.log(myFunction("aAbbcd"));

